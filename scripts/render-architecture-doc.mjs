import fs from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const repoRoot = path.resolve(__dirname, "..");

function list(values) {
  return values.map((value) => `- \`${value}\``).join("\n");
}

function plainList(values) {
  return values.map((value) => `- ${value}`).join("\n");
}

function definitionList(values) {
  return values.map((value) => `- \`${value.name}\`: ${value.definition}`).join("\n");
}

function nameRoleList(values) {
  return values.map((value) => `- \`${value.name}\`: ${value.role}`).join("\n");
}

export function renderArchitectureMarkdown(contract) {
  const ports = contract.ports
    .map((port) => {
      const adapters = port.requiredAdapters.map((adapter) => `  - \`${adapter}\``).join("\n");
      return [
        `### ${port.id}`,
        `- Port: \`${port.portFile}\``,
        `- Schema: \`${port.schemaFile}\``,
        "- Required adapters:",
        adapters || "  - None",
        `- Rule: ${port.swapRule}`
      ].join("\n");
    })
    .join("\n\n");

  const serviceModel = contract.serviceModel;
  const accessModel = contract.accessModel;
  const authnTarget = accessModel.authnTarget;
  const authzTarget = accessModel.authzTarget;
  const authzMapping = authzTarget.mapping
    .map(
      (entry) =>
        `- Neutrino \`${entry.neutrino}\` -> OpenFGA \`${entry.openFga}\`: ${entry.notes}`
    )
    .join("\n");

  return `# Architecture Canonical View

This document is generated from \`architecture/contract.json\`.  
Do not edit this file directly.

## Contract Metadata
- Name: \`${contract.name}\`
- Version: \`${contract.version}\`
- Last Updated: \`${contract.lastUpdated}\`

## Purpose
${contract.purpose}

## Principles
${list(contract.principles)}

## Standards
- Deployment:
  - Web: \`${contract.standards.deployment.web}\`
  - API: \`${contract.standards.deployment.api}\`
  - API Container: ${contract.standards.deployment.apiContainer}
- Design System:
  - Foundation: \`${contract.standards.designSystem.foundation}\`
  - Shared Package: \`${contract.standards.designSystem.sharedPackage}\`
  - Rule: ${contract.standards.designSystem.rule}

## Access Model
### Product Vocabulary
${plainList(accessModel.productVocabulary)}

### Rules
${plainList(accessModel.rules)}

### Authentication Target
- Provider: \`${authnTarget.provider}\`
- Role: ${authnTarget.role}
- Local fallback: ${authnTarget.localFallbackRule}

### Authorization Target
- Provider: \`${authzTarget.provider}\`
- Role: ${authzTarget.role}
- Not selected: ${authzTarget.notSelected}
- Builder rule: ${authzTarget.builderRule}

### OpenFGA Mapping
${authzMapping}

## Service Model
### User-Facing Vocabulary
${plainList(serviceModel.userFacingVocabulary)}

### Demoted Product Nouns
${plainList(serviceModel.demotedProductNouns)}

### Internal Kernel
${definitionList(serviceModel.internalKernel)}

### Service Identity
- Format: \`${serviceModel.serviceIdentity.format}\`
- Reserved namespaces:
${serviceModel.serviceIdentity.reservedNamespaces.map((entry) => `  - \`${entry.namespace}\`: ${entry.meaning}`).join("\n")}
- Workspace namespace rule: ${serviceModel.serviceIdentity.workspaceNamespaceRule}
- Shared service rule: ${serviceModel.serviceIdentity.sharedServiceRule}
- Private service rule: ${serviceModel.serviceIdentity.privateServiceRule}
- Examples:
${serviceModel.serviceIdentity.examples.map((example) => `  - \`${example}\``).join("\n")}

### Service Definition
- Manifest kind: \`${serviceModel.serviceDefinition.manifestKind}\`
- Required fields:
${serviceModel.serviceDefinition.requiredFields.map((field) => `  - \`${field}\``).join("\n")}
- Common fields:
${serviceModel.serviceDefinition.commonFields.map((field) => `  - \`${field}\``).join("\n")}
- Container packaging boundary: ${serviceModel.serviceDefinition.containerPackagingBoundary}

### Subordinate Concepts
${nameRoleList(serviceModel.subordinateConcepts)}

### Recipe Boundary
${serviceModel.recipeBoundary}

### Execution Invariants
${plainList(serviceModel.executionInvariants)}

### Execution References
${plainList(serviceModel.executionMustReference)}

### Record And Storage Model
${plainList(serviceModel.recordAndStorageModel)}

## Runtime Boundaries
${list(contract.runtimeBoundaries.apps)}
- \`schemaRoot\`: \`${contract.runtimeBoundaries.schemaRoot}\`
- \`portsRoot\`: \`${contract.runtimeBoundaries.portsRoot}\`
- \`coreRoot\`: \`${contract.runtimeBoundaries.coreRoot}\`
- \`adaptersRoot\`: \`${contract.runtimeBoundaries.adaptersRoot}\`
${list(contract.runtimeBoundaries.sharedPackages)}

## Ports And Adapters
${ports}

## Required Paths
${list(contract.requiredPaths)}

## Drift Enforcement
- Generated doc: \`${contract.driftEnforcement.generatedDoc}\`
- Render script: \`${contract.driftEnforcement.renderScript}\`
- Check script: \`${contract.driftEnforcement.checkScript}\`
- Workflow: \`${contract.driftEnforcement.workflow}\`
`;
}

async function main() {
  const contractPath = path.join(repoRoot, "architecture", "contract.json");
  const outputPath = path.join(repoRoot, "docs", "architecture-canonical.md");

  const rawContract = await fs.readFile(contractPath, "utf8");
  const contract = JSON.parse(rawContract);

  const markdown = renderArchitectureMarkdown(contract);
  await fs.mkdir(path.dirname(outputPath), { recursive: true });
  await fs.writeFile(outputPath, markdown, "utf8");
  process.stdout.write(`Wrote ${path.relative(repoRoot, outputPath)}\n`);
}

if (process.argv[1] && path.resolve(process.argv[1]) === __filename) {
  main().catch((error) => {
    console.error(error);
    process.exit(1);
  });
}
